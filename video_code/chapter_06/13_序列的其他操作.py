# 序列相加
# 列表
list1 = [1,2,3,4]
list2 = [5,6,7,8]
list3 = list1+list2
print(list3)

# 元组
tuple1 = (1,2,3,4)
tuple2 = (5,6,7,8)
tuple3 = tuple1+tuple2
print(tuple3)

# 字符串
str1 = '1234'
str2 = '5678'
str3 = str1+str2
print(str3)

